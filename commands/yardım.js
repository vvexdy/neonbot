const { EmbedBuilder } = require("discord.js");

exports.run = async (client, message, args) => {

  const embed = new EmbedBuilder()
  .setColor("#EB459E")
  .setTitle(`${message.guild.name} | Commands`)
  .setThumbnail(`${message.author.displayAvatarURL({ dynamic: true })}`)
  .setDescription("**-kayýt-yardým** -> Kayýt komutlarýný goruntuler \n **-abone-yardým** -> Abone komutlarýný goruntuler \n **-baþvuru-yardým** -> Baþvuru komutlarýný görüntüler") 
  .setFooter({ text: `Asked by ${message.author.tag}.`, iconURL: `${message.author.displayAvatarURL({ dynamic: true })}` })    
  .setTimestamp()  

  return message.reply({ embeds: [embed] })

};
exports.conf = {
  aliases: ["yardým"]
};

exports.help = {
  name: "help"
};